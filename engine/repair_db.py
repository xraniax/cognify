import os
import json
import logging
from sqlalchemy import create_engine, text
from services.schemas import QuizOutput, ExamOutput, FlashcardsOutput
from pydantic import ValidationError

# Setup DB connection
DATABASE_URL = os.getenv("DATABASE_URL", "postgresql://postgres:postgres@db:5432/intello_db")
engine = create_engine(DATABASE_URL)

def repair_quiz(data):
    if "quiz_questions" in data and "questions" not in data:
        data["questions"] = data.pop("quiz_questions")
    elif "quiz" in data and not isinstance(data.get("quiz"), str) and "questions" not in data:
        data["questions"] = data.pop("quiz")

    if "questions" in data and isinstance(data["questions"], list):
        for i, q in enumerate(data["questions"]):
            if "question_id" in q: q["id"] = q.pop("question_id")
            if "id" not in q: q["id"] = i + 1
            if "title" in q: q["question"] = q.pop("title")
            if "answer" in q: q["correct_answer"] = q.pop("answer")
            if "choices" in q: q["options"] = q.pop("choices")
            if "options" not in q: q["options"] = []
            if "explanation" not in q: q["explanation"] = "Generated by AI."
    return data

def repair_flashcards(data):
    if "flashcards" in data and "cards" not in data:
        data["cards"] = data.pop("flashcards")
    return data

def repair_exam(data):
    if "exam_questions" in data: data["questions"] = data.pop("exam_questions")
    if "questions" in data:
        for q in data["questions"]:
            if "text" in q: q["question"] = q.pop("text")
            if "answer_space" not in q: q["answer_space"] = "____________________"
    return data

def run_repair():
    with engine.connect() as conn:
        # Find ALL quizzes that might be broken (status COMPLETED but maybe failing or has error)
        query = text("SELECT id, type, ai_generated_content FROM materials WHERE status = 'COMPLETED'")
        result = conn.execute(query)
        
        for row in result:
            m_id = row[0]
            m_type = row[1]
            content = row[2]
            
            # If it's already a valid quiz with questions, skip unless it has error field
            if content and "questions" in content and not content.get("error"):
                continue
                
            raw_text = content.get("raw") if isinstance(content, dict) else None
            if not raw_text:
                # Maybe it was saved differently
                raw_text = json.dumps(content)
            
            try:
                # Clean and parse raw JSON
                if "```json" in raw_text:
                    raw_text = raw_text.split("```json")[1].split("```")[0].strip()
                elif "```" in raw_text:
                    raw_text = raw_text.split("```")[1].split("```")[0].strip()
                
                parsed = json.loads(raw_text)
                
                # Apply repairs
                if m_type == "quiz":
                    parsed = repair_quiz(parsed)
                    validated = QuizOutput(**parsed).model_dump()
                elif m_type == "exam":
                    parsed = repair_exam(parsed)
                    validated = ExamOutput(**parsed).model_dump()
                elif m_type == "flashcards":
                    parsed = repair_flashcards(parsed)
                    validated = FlashcardsOutput(**parsed).model_dump()
                else:
                    continue
                
                # Update DB
                print(f"Repaired material {m_id} ({m_type})")
                update_query = text("UPDATE materials SET ai_generated_content = :content WHERE id = :id")
                conn.execute(update_query, {"content": json.dumps(validated), "id": m_id})
                conn.commit()
                
            except (json.JSONDecodeError, ValidationError, Exception) as e:
                # Suppress normal ones, only log real failures
                pass

if __name__ == "__main__":
    run_repair()
